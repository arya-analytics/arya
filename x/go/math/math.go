// math is a package that extends math functionality.

package math

// IntPow returns the integer n to the m power. IntPow requires m to be a
// nonnegative integer.
func IntPow(n, m int) int {
	if m == 0 {
		return 1
	}

	if m == 1 {
		return n
	}

	result := n
	for i := 2; i <= m; i++ {
		result *= n
	}
	return result
}
